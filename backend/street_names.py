# Убираем префиксы вул./пр./пл. перед сравнением
_PREFIX_RE = re.compile(
    r"^(вулиця|вул\.?|проспект|пр\.?|провулок|пров\.?|"
    r"площа|пл\.?|бульвар|бул\.?|шосе|набережна|узвіз|ул\.?)\s+",
    re.IGNORECASE | re.UNICODE,
)
_SPACES = re.compile(r"\s+")


def _clean(s: str) -> str:
    s = _PREFIX_RE.sub("", s.strip().lower())
    return _SPACES.sub(" ", s)


def normalize_street(name: str) -> tuple[str, bool]:
    """
    Приводит старое/русское название к текущему официальному.
    Возвращает (normalized_name, was_renamed).
    Если переименования нет — возвращает оригинал без изменений.
    """
    if not name:
        return name, False

    key = _clean(name)

    # Точное совпадение
    if key in RENAME_TABLE:
        new = RENAME_TABLE[key]
        logger.info("Rename exact: '%s' → '%s'", name, new)
        return new, True

    # Частичное совпадение (название содержит известный ключ)
    for old, new in RENAME_TABLE.items():
        if old in key:
            logger.info("Rename partial: '%s' matched '%s' → '%s'", name, old, new)
            return new, True

    return name, False


def get_display_name(original: str, normalized: str, was_renamed: bool) -> str:
    """
    Для попапа: показываем обе формы чтобы пользователь узнал место.
    Например: 'Жукова → вул. Героїв Крут'
    """
    if not was_renamed or original.strip().lower() == normalized.strip().lower():
        return original
    return f"{original}  →  {normalized}"


